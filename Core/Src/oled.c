void OLED_WriteCmd(uint8_t cmd)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(OLED_ADDR << 1);
    OLED_I2C_WaitAck();
    OLED_I2C_SendByte(OLED_CMD);
    OLED_I2C_WaitAck();
    OLED_I2C_SendByte(cmd);
    OLED_I2C_WaitAck();
    OLED_I2C_Stop();
}

static void OLED_WriteData(uint8_t data)
{
    OLED_I2C_Start();
    OLED_I2C_SendByte(OLED_ADDR << 1);
    OLED_I2C_WaitAck();
    OLED_I2C_SendByte(OLED_DATA);
    OLED_I2C_WaitAck();
    OLED_I2C_SendByte(data);
    OLED_I2C_WaitAck();
    OLED_I2C_Stop();
}

