void OLED_SetDirection(uint8_t flag)
{
    if(flag)
    {
        OLED_WriteCmd(0xC8); // 0xC8:UP  ->Down , 0xC0: Reverse
        OLED_WriteCmd(0xA1); // 0xA1:Left->Right, 0xA0: Horization Mirror
    }
    else
    {
        OLED_WriteCmd(0xC0); // 0xC8:UP  ->Down , 0xC0: Reverse
        OLED_WriteCmd(0xA0); // 0xA1:Left->Right, 0xA0: Horization Mirror                
    }
}

void OLED_Clear(void)
{
    uint8_t i, j;
    for (i = 0; i < 8; i++)
    {
        OLED_WriteCmd(0xB0 + i);
        OLED_WriteCmd(0x02);
        OLED_WriteCmd(0x10);
        for (j = 0; j < 128; j++)
            OLED_WriteData(0);
    }
}

void OLED_SetPos(uint8_t x, uint8_t y)
{
    OLED_WriteCmd(0xB0 + y);
    OLED_WriteCmd(((x + 2) & 0xF0) >> 4 | 0x10);
    OLED_WriteCmd((x + 2) & 0x0F);
}

void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t chr, uint8_t size)
{
    uint8_t i;
    uint8_t c = chr - ' ';
    if (x > 126) { x = 0; y += 2; }
    if (size == 16)
    {
        OLED_SetPos(x, y);
        for (i = 0; i < 8; i++)
            OLED_WriteData(F8X16[c * 16 + i]);
        OLED_SetPos(x, y + 1);
        for (i = 0; i < 8; i++)
            OLED_WriteData(F8X16[c * 16 + i + 8]);
    }
    else
    {
        OLED_SetPos(x, y);
        for (i = 0; i < 6; i++)
            OLED_WriteData(F6x8[c][i]);
    }
}

void OLED_ShowString(uint8_t x, uint8_t y, uint8_t *str, uint8_t size)
{
    while (*str)
    {
        OLED_ShowChar(x, y, *str++, size);
        x += 8;
        if (x > 120) { x = 0; y += 2; }
    }
}

uint32_t oled_pow(uint8_t m, uint8_t n)
{
    uint32_t result = 1;
    while (n--) result *= m;
    return result;
}

void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size)
{
    uint8_t t, temp, enshow = 0;
    for (t = 0; t < len; t++)
    {
        temp = (num / oled_pow(10, len - t - 1)) % 10;
        if (enshow == 0 && t < (len - 1))
        {
            if (temp == 0)
            {
                OLED_ShowChar(x + (size / 2) * t, y, ' ', size);
                continue;
            }
            else enshow = 1;
        }
        OLED_ShowChar(x + (size / 2) * t, y, temp + '0', size);
    }
}



